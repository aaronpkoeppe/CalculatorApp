/**
 * Interface AdvancedMath sets standards for AdvancedCalc to follow to allow the use of advanced mathematical question such as, to the power and square root.
 */
public interface AdvancedMath {
    /**
     * all doubles within the AdvancedMath interface are described within the AdvancedCalc.java file.
     */
    double pow(double x, double y);

    double sqrt(double x);
}
